interface NavBarProps {
  role?: 'parent' | 'child'
}

export function NavBar({ role }: NavBarProps) {
  const pathname = usePathname()

  // Parents get a Chat nav item; kids use the Quest Buddy FAB on the quests page instead
  const navItems = role === 'parent' ? [...BASE_NAV_ITEMS, CHAT_NAV_ITEM] : BASE_NAV_ITEMS

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 safe-area-bottom">
      <div className="flex justify-around items-center h-16 max-w-lg mx-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex flex-col items-center justify-center w-16 h-full transition-colors',
                isActive ? 'text-purple-600' : 'text-gray-400 hover:text-gray-600'
              )}
            >
              {item.icon}
              <span className="text-xs mt-1 font-medium">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
